#pragma once

#include <cstdint>
// Подключает фиксированные целочисленные типы, такие как std::int64_t.
// Это гарантирует одинаковый размер числа на всех платформах.
namespace chatserver::domain {
// Пространство имен domain - слой предметной области (DDD).
// UserId - Value Object, представляющий идентификатор пользователя.
class UserId {
// Класс инкапсулирует идентификатор пользователя как неизменяемое значение.
public:
    explicit UserId(std::int64_t value);
    // Конструктор принимает 64-битное целое число.
    // explicit предотвращает неявные преобразования из int64_t в UserId,
    // что повышает типобезопасность.
    std::int64_t value() const;
    // Геттер, возвращающий внутреннее значение идентификатора.
    // Возвращается по значению, так как это примитивный тип.
    bool operator==(const UserId& other) const;
    // Оператор сравнения на равенство.
    // Позволяет сравнивать два UserId напрямую.
    bool operator<(const UserId& other) const;
    // Оператор "меньше".
    // Нужен для использования UserId в std::map, std::set и сортировках.

private:
    std::int64_t value_;
    // Внутреннее хранилище идентификатора пользователя.
    // int64_t обеспечивает большой диапазон значений.
};

}

