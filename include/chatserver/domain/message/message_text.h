#pragma once

#include <string>

namespace chatserver::domain {
// Пространство имён domain — слой предметной области (DDD).
// MessageText — Value Object, представляющий текст сообщения.

class MessageText {
// Класс инкапсулирует текст сообщения как неизменяемое значение.
// Это типичный Value Object: всегда валиден, сравнивается по значению,
// не имеет собственной идентичности.
public:
    explicit MessageText(std::string value);
    // Конструктор принимает строку с текстом сообщения.
    // explicit предотвращает неявные преобразования из std::string в MessageText,
    // что повышает типобезопасность и защищает от случайных ошибок.
    const std::string& value() const;
    // Геттер, возвращающий ссылку на внутреннюю строку.
    // const std::string& исключает копирование 
    // и не позволяет вызывающему коду изменить текст.
private:
    std::string value_;
    // Внутреннее хранилище текста сообщения.
    // std::string подходит, так как сообщение — текстовое значение.
};
}

